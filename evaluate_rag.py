# Main
# ---------------------------------------------------------------------------


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Evaluate the Financial Reports Knowledge Assistant against 20 ground-truth questions."
    )
    parser.add_argument(
        "--eval-dir",
        type=Path,
        default=DEFAULT_EVAL_DIR,
        help=f"Directory containing fin_report_*.json eval files (default: {DEFAULT_EVAL_DIR})",
    )
    parser.add_argument(
        "--results-dir",
        type=Path,
        default=DEFAULT_RESULTS_DIR,
        help=f"Directory to write eval_results.json (default: {DEFAULT_RESULTS_DIR})",
    )
    parser.add_argument(
        "--endpoint",
        default=ENDPOINT_NAME,
        help=f"Knowledge Assistant endpoint name (default: {ENDPOINT_NAME})",
    )
    parser.add_argument(
        "--verbose",
        action="store_true",
        help="Print full answers and reasoning for each question",
    )
    parser.add_argument(
        "--no-judge",
        action="store_true",
        help="Skip LLM judge scoring (record answers only)",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()

    print("=" * 60)
    print("Financial Reports Assistant — RAG Evaluation")
    print("=" * 60)
    print(f"Host         : {HOST}")
    print(f"Endpoint     : {args.endpoint}")
    print(f"Eval dir     : {args.eval_dir}")
    print(f"Results dir  : {args.results_dir}")
    print()

    # Connect
    print("Connecting to Databricks workspace…")
    client = WorkspaceClient(host=HOST)
    print("Connected.\n")

    # Load eval cases
    eval_cases = load_eval_cases(args.eval_dir)

    # Create results directory
    args.results_dir.mkdir(parents=True, exist_ok=True)

    results: list[dict[str, Any]] = []
    errors:  list[str]            = []

    for i, case in enumerate(eval_cases, start=1):
        title     = case.get("title", f"Document {i}")
        question  = case.get("question", "")
        guideline = case.get("guideline", "")
        pdf_path  = case.get("pdf_path", "")

        print(f"[{i:02d}/{len(eval_cases)}] {title}")
        print(f"         Q: {question}")

        # 1. Query the Knowledge Assistant
        try:
            answer, citations = query_knowledge_assistant(client, question, args.endpoint)
        except Exception as exc:
            err_msg = f"Query failed: {exc}"
            print(f"         ERROR: {err_msg}\n")
            errors.append(f"Case {i} ({title}): {err_msg}")
            results.append({
                "index":     i,
                "title":     title,
                "question":  question,
                "guideline": guideline,
                "pdf_path":  pdf_path,
                "answer":    "",
                "citations": [],
                "score":     0,
                "reasoning": err_msg,
                "error":     True,
            })
            continue

        if args.verbose:
            print(f"         A: {answer[:300]}{'…' if len(answer) > 300 else ''}")

        # 2. Score with LLM judge (unless suppressed)
        score, reasoning = 0, "Scoring skipped (--no-judge)"
        if not args.no_judge and question and guideline:
            try:
                judgment  = judge_answer(client, question, guideline, answer)
                score     = judgment["score"]
                reasoning = judgment["reasoning"]
            except Exception as exc:
                reasoning = f"Judge failed: {exc}"
                errors.append(f"Case {i} ({title}): judge error — {exc}")

        verdict = "PASS" if score == 1 else "FAIL"
        print(f"         Result: {verdict}  |  {reasoning[:80]}{'…' if len(reasoning) > 80 else ''}\n")

        results.append({
            "index":     i,
            "title":     title,
            "question":  question,
            "guideline": guideline,
            "pdf_path":  pdf_path,
            "answer":    answer,
            "citations": citations,
            "score":     score,
            "reasoning": reasoning,
            "error":     False,
        })

    # Summary table
    print_summary_table(results)

    # Write results file
    output = {
        "run_timestamp": datetime.now(timezone.utc).isoformat(),
        "host":          HOST,
        "endpoint":      args.endpoint,
        "total":         len(results),
        "passed":        sum(1 for r in results if r["score"] == 1),
        "failed":        sum(1 for r in results if r["score"] == 0),
        "errors":        errors,
        "results":       results,
    }
    out_path = args.results_dir / "eval_results.json"
    with open(out_path, "w") as f:
        json.dump(output, f, indent=2)
    print(f"Full results written to: {out_path}")

    # Exit with non-zero if more than half failed (useful in CI)
    pass_rate = output["passed"] / output["total"] if output["total"] else 0
    if pass_rate < 0.5:
        print(f"\nWARNING: Pass rate {pass_rate:.0%} is below 50%. Check endpoint and document indexing.")
        sys.exit(1)


if __name__ == "__main__":
    main()
