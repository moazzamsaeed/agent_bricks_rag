"""
RAG Evaluation Harness — Financial Reports Assistant
=====================================================
Loads the 20 companion eval JSON files produced by generate_pdfs.py, queries
the Financial Reports Assistant Knowledge Assistant endpoint for each question,
scores each answer against its human-written guideline using an LLM-as-judge,
and writes a full results file plus a printed summary table.

Usage:
    python evaluate_rag.py [--eval-dir PATH] [--results-dir PATH] [--verbose]

Environment:
    DATABRICKS_HOST   Workspace URL  (or set in ~/.databrickscfg)
    DATABRICKS_TOKEN  Personal access token (or set in ~/.databrickscfg)
"""

from __future__ import annotations

import argparse
import json
import os
import sys
import time
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

HOST          = "https://adb-6417907769725610.10.azuredatabricks.net"
ENDPOINT_NAME = "ka-e53ea1a5-endpoint"

# Path to the companion JSON files generated by generate_pdfs.py
DEFAULT_EVAL_DIR    = Path(__file__).parent.parent / "pdf-gen-rag" / "output"
DEFAULT_RESULTS_DIR = Path(__file__).parent / "results"

# Model used to judge answer quality (must be available on the workspace)
JUDGE_MODEL = "databricks-meta-llama-3-3-70b-instruct"

JUDGE_PROMPT_TEMPLATE = """\
You are an expert evaluator assessing the quality of an AI assistant's answer.

Question: {question}

Guideline (what a correct answer must include):
{guideline}

Assistant answer:
{answer}

Evaluate whether the assistant's answer satisfies the guideline.
Respond with a JSON object with exactly these fields:
{{
  "score": <0 or 1>,
  "reasoning": "<one or two sentences explaining your decision>"
}}

Score 1 if the answer substantially satisfies the guideline.
Score 0 if it is missing key required information or is factually wrong.
Return only valid JSON, no markdown fences."""

# ---------------------------------------------------------------------------
# Imports (deferred to give a clean error if SDK is missing)
# ---------------------------------------------------------------------------

try:
    from databricks.sdk import WorkspaceClient
    from databricks.sdk.service.serving import ChatMessage, ChatMessageRole
except ImportError:
    print("ERROR: databricks-sdk is not installed. Run: pip install databricks-sdk>=0.20.0")
    sys.exit(1)

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def load_eval_cases(eval_dir: Path) -> list[dict[str, Any]]:
    """Load all fin_report_*.json files from eval_dir and return as a list."""
    paths = sorted(eval_dir.glob("fin_report_*.json"))
    if not paths:
        raise FileNotFoundError(
            f"No fin_report_*.json files found in {eval_dir}.\n"
            "Run generate_pdfs.py first, or set --eval-dir to the correct path."
        )
    cases = []
    for p in paths:
        with open(p) as f:
            cases.append(json.load(f))
    print(f"Loaded {len(cases)} evaluation cases from {eval_dir}")
    return cases


def query_knowledge_assistant(
    client: WorkspaceClient,
    question: str,
    endpoint_name: str = ENDPOINT_NAME,
    retries: int = 3,
) -> tuple[str, list[str]]:
    """
    Query the Knowledge Assistant endpoint.

    Returns:
        (answer_text, list_of_source_citations)
    """
    for attempt in range(1, retries + 1):
        try:
            resp = client.api_client.do(
                "POST",
                f"/serving-endpoints/{endpoint_name}/invocations",
                body={"input": [{"role": "user", "content": question}]},
            )

            # Parse OpenAI Responses API format returned by Agent Bricks KA
            answer_parts: list[str] = []
            citations: list[str] = []
            for msg in resp.get("output", []):
                for part in msg.get("content", []):
                    if part.get("type") == "output_text":
                        answer_parts.append(part.get("text", ""))
                        for ann in part.get("annotations", []):
                            title = ann.get("title", "")
                            if title and title not in citations:
                                citations.append(title)

            return "".join(answer_parts), citations

        except Exception as exc:
            if attempt == retries:
                raise
            wait = 2 ** attempt
            print(f"    Attempt {attempt} failed ({exc}). Retrying in {wait}s…")
            time.sleep(wait)

    return "", []


def judge_answer(
    client: WorkspaceClient,
    question: str,
    guideline: str,
    answer: str,
) -> dict[str, Any]:
    """
    Use an LLM-as-judge to score whether `answer` satisfies `guideline`.

    Returns a dict with keys: score (0 or 1), reasoning (str)
    """
    prompt = JUDGE_PROMPT_TEMPLATE.format(
        question=question,
        guideline=guideline,
        answer=answer,
    )
    response = client.serving_endpoints.query(
        name=JUDGE_MODEL,
        messages=[ChatMessage(role=ChatMessageRole.USER, content=prompt)],
        max_tokens=256,
        temperature=0.0,
    )
    raw = response.choices[0].message.content.strip()

    # Strip markdown fences if model wraps response
    if raw.startswith("```"):
        raw = raw.split("```")[1]
        if raw.startswith("json"):
            raw = raw[4:]
        raw = raw.strip()

    try:
        result = json.loads(raw)
        return {
            "score":     int(result.get("score", 0)),
            "reasoning": str(result.get("reasoning", "")),
        }
    except (json.JSONDecodeError, ValueError):
        # Fallback: attempt to extract score from raw text
        score = 1 if '"score": 1' in raw or "'score': 1" in raw else 0
        return {"score": score, "reasoning": raw[:200]}


def print_summary_table(results: list[dict[str, Any]]) -> None:
    """Print a formatted results table to stdout."""
    col_widths = [4, 40, 7, 60]
    header = f"{'#':<{col_widths[0]}}  {'Document':<{col_widths[1]}}  {'Score':<{col_widths[2]}}  {'Reasoning':<{col_widths[3]}}"
    separator = "-" * (sum(col_widths) + 6)

    print("\n" + separator)
    print(header)
    print(separator)

    for r in results:
        score_str = "PASS" if r["score"] == 1 else "FAIL"
        title = r["title"][:38] + ".." if len(r["title"]) > 40 else r["title"]
        reasoning = r["reasoning"][:58] + ".." if len(r["reasoning"]) > 60 else r["reasoning"]
        print(f"{r['index']:<{col_widths[0]}}  {title:<{col_widths[1]}}  {score_str:<{col_widths[2]}}  {reasoning}")

    total   = len(results)
    passed  = sum(1 for r in results if r["score"] == 1)
    failed  = total - passed
    pct     = (passed / total * 100) if total else 0

    print(separator)
    print(f"\nResults: {passed}/{total} PASS ({pct:.1f}%)   |   {failed} FAIL")
    print()


